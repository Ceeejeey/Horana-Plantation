export function getMetricsByCategory(category: TenYearCategory): TenYearMetric[] {
  return TEN_YEAR_METRICS.filter((m) => m.category === category);
}

export function getMetricById(id: string): TenYearMetric | undefined {
  return TEN_YEAR_METRICS.find((m) => m.id === id);
}

export function metricToChartData(metric: TenYearMetric): { year: (typeof TEN_YEAR_FISCAL_YEARS)[number]; value: number }[] {
  return TEN_YEAR_FISCAL_YEARS.flatMap((year) => {
    const value = metric.values[year];
    if (value == null) return [];
    return [{ year, value }];
  });
}